// @ts-check
const { withXcodeProject } = require('@expo/config-plugins');
const path = require('path');
const fs = require('fs');

const WIDGET_NAME = 'PomodoroWidget';
const WIDGET_BUNDLE_ID = 'com.roysung.roPomodoroNative.widget';
const APP_GROUP = 'group.com.roysung.roPomodoroNative';
const DEPLOYMENT_TARGET = '17.0';
const WIDGET_SOURCE_DIR = path.join(__dirname, '..', 'ios-widget', WIDGET_NAME);
// Files in the widget folder that must ALSO be compiled into the main app
// target. AppIntents with `openAppWhenRun = true` run their perform() in the
// app process, so the type must exist in both binaries.
const SHARED_WITH_APP_TARGET = ['PomodoroIntents.swift'];

const GENERATED_HEADER = `// AUTO-GENERATED by plugins/with-ios-widget.js from ios-widget/${WIDGET_NAME}/.
// Edits to this copy will be overwritten on the next \`expo prebuild\`.
// Edit the source under apps/native/ios-widget/${WIDGET_NAME}/ instead.

`;

function copyWidgetFiles(iosDir) {
  const dest = path.join(iosDir, WIDGET_NAME);
  if (!fs.existsSync(dest))
    fs.mkdirSync(dest, { recursive: true });

  for (const file of fs.readdirSync(WIDGET_SOURCE_DIR)) {
    const srcFile = path.join(WIDGET_SOURCE_DIR, file);
    if (!fs.statSync(srcFile).isFile())
      continue;

    const destFile = path.join(dest, file);
    if (file.endsWith('.swift')) {
      const content = fs.readFileSync(srcFile, 'utf8');
      fs.writeFileSync(destFile, GENERATED_HEADER + content);
    } else {
      fs.copyFileSync(srcFile, destFile);
    }
  }
}

function writeEntitlements(iosDir) {
  const entitlementsPath = path.join(iosDir, WIDGET_NAME, `${WIDGET_NAME}.entitlements`);
  fs.writeFileSync(
    entitlementsPath,
    `<?xml version="1.0" encoding="UTF-8"?>
<!DOCTYPE plist PUBLIC "-//Apple//DTD PLIST 1.0//EN" "http://www.apple.com/DTDs/PropertyList-1.0.dtd">
<plist version="1.0">
<dict>
  <key>com.apple.security.application-groups</key>
  <array>
    <string>${APP_GROUP}</string>
  </array>
</dict>
</plist>`,
  );
}

/**
 * Add shared widget files (intents) to the main app target's Sources build
 * phase by creating a new PBXBuildFile that references the same fileRef
 * the widget target uses. Idempotent.
 */
function addSharedFilesToAppTarget(xcodeProject) {
  const appTarget = xcodeProject.getTarget('com.apple.product-type.application');
  if (!appTarget)
    return;
  const sourcesPhase = xcodeProject.pbxSourcesBuildPhaseObj(appTarget.uuid);
  if (!sourcesPhase || !Array.isArray(sourcesPhase.files))
    return;

  const buildFileSection = xcodeProject.pbxBuildFileSection();
  const fileRefSection = xcodeProject.pbxFileReferenceSection();

  const existing = new Set();
  for (const entry of sourcesPhase.files) {
    const bf = buildFileSection[entry.value];
    if (!bf)
      continue;
    const ref = fileRefSection[bf.fileRef];
    if (!ref || typeof ref !== 'object')
      continue;
    const p = (ref.path || ref.name || '').replace(/^"|"$/g, '');
    existing.add(path.basename(p));
  }

  for (const name of SHARED_WITH_APP_TARGET) {
    if (existing.has(name))
      continue;
    let fileRefUuid = null;
    for (const key of Object.keys(fileRefSection)) {
      if (key.endsWith('_comment'))
        continue;
      const ref = fileRefSection[key];
      if (!ref || typeof ref !== 'object')
        continue;
      const p = (ref.path || ref.name || '').replace(/^"|"$/g, '');
      if (path.basename(p) === name) {
        fileRefUuid = key;
        break;
      }
    }
    if (!fileRefUuid)
      continue;

    const buildFileUuid = xcodeProject.generateUuid();
    const commentText = `${name} in Sources`;
    buildFileSection[buildFileUuid] = {
      isa: 'PBXBuildFile',
      fileRef: fileRefUuid,
      fileRef_comment: name,
    };
    buildFileSection[`${buildFileUuid}_comment`] = commentText;
    sourcesPhase.files.push({ value: buildFileUuid, comment: commentText });
  }
}

/**
 * On repeat prebuilds, scan the widget folder and add any source/resource
 * files that aren't already referenced by the widget target. Keeps the
 * Xcode target in sync without re-creating it (preserves signing etc.).
 */
/** xcode-node's pbxTargetByName returns the target object without exposing
 *  the section UUID (the UUID *is* the section key). Look it up by walking
 *  the PBXNativeTarget section's comment entries. */
function findTargetUuid(xcodeProject, targetName) {
  const section = xcodeProject.pbxNativeTargetSection();
  for (const key of Object.keys(section)) {
    if (key.endsWith('_comment'))
      continue;
    if (section[`${key}_comment`] === targetName)
      return key;
  }
  return null;
}

function reconcileWidgetSources(xcodeProject, iosDir) {
  const targetUuid = findTargetUuid(xcodeProject, WIDGET_NAME);
  if (!targetUuid)
    return;

  // Look up the PBX group named "PomodoroWidget" (created on first run)
  const groups = xcodeProject.hash.project.objects.PBXGroup || {};
  /** @type {string | null} */
  let groupUuid = null;
  for (const key of Object.keys(groups)) {
    if (key.endsWith('_comment'))
      continue;
    const g = groups[key];
    if (g && g.path === WIDGET_NAME) {
      groupUuid = key;
      break;
    }
  }

  const sourcesPhase = xcodeProject.pbxSourcesBuildPhaseObj(targetUuid);
  const resourcesPhase = xcodeProject.pbxResourcesBuildPhaseObj(targetUuid);

  /** @returns {Set<string>} basenames already in the given phase */
  const phaseFileNames = (phase) => {
    const result = new Set();
    if (!phase || !phase.files)
      return result;
    const buildFileSection = xcodeProject.pbxBuildFileSection();
    const fileRefSection = xcodeProject.pbxFileReferenceSection();
    for (const entry of phase.files) {
      const buildFile = buildFileSection[entry.value];
      if (!buildFile || !buildFile.fileRef)
        continue;
      const ref = fileRefSection[buildFile.fileRef];
      if (!ref)
        continue;
      const p = (ref.path || '').replace(/^"|"$/g, '');
      const base = path.basename(p);
      if (base)
        result.add(base);
    }
    return result;
  };

  const existingSrcs = phaseFileNames(sourcesPhase);
  const existingRess = phaseFileNames(resourcesPhase);

  const files = fs.readdirSync(path.join(iosDir, WIDGET_NAME));
  const newSwift = files.filter(
    f => f.endsWith('.swift') && !existingSrcs.has(f),
  );
  const newResources = files.filter(
    f => !f.endsWith('.swift')
      && f !== 'Info.plist'
      && !f.endsWith('.entitlements')
      && !existingRess.has(f),
  );

  for (const f of newSwift) {
    xcodeProject.addSourceFile(
      f,
      { target: targetUuid },
      groupUuid || undefined,
    );
  }
  for (const f of newResources) {
    xcodeProject.addResourceFile(
      f,
      { target: targetUuid },
      groupUuid || undefined,
    );
  }
}

/**
 * @param {import('@expo/config-plugins').ExpoConfig} config
 */
const withIosWidget = (config) => {
  return withXcodeProject(config, (mod) => {
    const xcodeProject = mod.modResults;
    const iosDir = mod.modRequest.platformProjectRoot;

    // 1. Copy Swift source files + write entitlements to disk first
    copyWidgetFiles(iosDir);
    writeEntitlements(iosDir);

    // 2. On repeated prebuild runs the target already exists — reconcile
    //    sources/resources so newly added Swift files (e.g. Live Activity)
    //    still get picked up without requiring `--clean`.
    if (xcodeProject.pbxTargetByName(WIDGET_NAME)) {
      reconcileWidgetSources(xcodeProject, iosDir);
      addSharedFilesToAppTarget(xcodeProject);
      return mod;
    }

    // Bare filenames — the PBX group's own path property supplies the directory.
    // Using "PomodoroWidget/file.swift" here would cause addPbxGroup to nest
    // inside path=PomodoroWidget, producing double-prefixed paths at build time.
    const swiftFileNames = fs
      .readdirSync(path.join(iosDir, WIDGET_NAME))
      .filter(f => f.endsWith('.swift'));

    const entitlementsRelPath = `${WIDGET_NAME}/${WIDGET_NAME}.entitlements`;
    const plistRelPath = `${WIDGET_NAME}/Info.plist`;

    // 3. Add widget extension target
    const target = xcodeProject.addTarget(
      WIDGET_NAME,
      'app_extension',
      WIDGET_NAME,
      WIDGET_BUNDLE_ID,
    );

    // 4. Add source + resource build phases — paths relative to project root
    const resourceFileNames = fs
      .readdirSync(path.join(iosDir, WIDGET_NAME))
      .filter(f => !f.endsWith('.swift') && f !== 'Info.plist' && !f.endsWith('.entitlements'));

    const swiftFilesFromRoot = swiftFileNames.map(f => `${WIDGET_NAME}/${f}`);
    xcodeProject.addBuildPhase(swiftFilesFromRoot, 'PBXSourcesBuildPhase', 'Sources', target.uuid);
    xcodeProject.addBuildPhase(
      resourceFileNames.map(f => `${WIDGET_NAME}/${f}`),
      'PBXResourcesBuildPhase', 'Resources', target.uuid,
    );

    // 5. Add files to PBX group using bare names; group path="PomodoroWidget" resolves them
    const group = xcodeProject.addPbxGroup(
      [...swiftFileNames, ...resourceFileNames, 'Info.plist', `${WIDGET_NAME}.entitlements`],
      WIDGET_NAME,
      WIDGET_NAME,
    );
    const mainGroupKey
      = xcodeProject.getFirstProject().firstProject.mainGroup;
    xcodeProject.addToPbxGroup(group.uuid, mainGroupKey);

    // 6. Set build settings on every configuration that belongs to this target
    const configurations = xcodeProject.pbxXCBuildConfigurationSection();
    const targetConfigListUuid
      = xcodeProject.pbxNativeTargetSection()[target.uuid]?.buildConfigurationList;

    for (const key of Object.keys(configurations)) {
      const cfg = configurations[key];
      if (typeof cfg !== 'object' || !cfg.buildSettings)
        continue;

      // Match by build configuration list membership when possible
      if (
        targetConfigListUuid
        && !xcodeProject
          .pbxXCConfigurationList()[targetConfigListUuid]
          ?.buildConfigurations?.some?.(
            (/** @type {{ value: string }} */ ref) => ref.value === key,
          )
      ) {
        continue;
      }

      if (cfg.buildSettings.PRODUCT_NAME !== `"${WIDGET_NAME}"`)
        continue;

      cfg.buildSettings.SWIFT_VERSION = '5.9';
      cfg.buildSettings.IPHONEOS_DEPLOYMENT_TARGET = DEPLOYMENT_TARGET;
      cfg.buildSettings.INFOPLIST_FILE = `"${plistRelPath}"`;
      cfg.buildSettings.CODE_SIGN_ENTITLEMENTS = `"${entitlementsRelPath}"`;
      cfg.buildSettings.GENERATE_INFOPLIST_FILE = 'NO';
      cfg.buildSettings.SKIP_INSTALL = 'YES';
    }

    addSharedFilesToAppTarget(xcodeProject);

    return mod;
  });
};

module.exports = withIosWidget;
